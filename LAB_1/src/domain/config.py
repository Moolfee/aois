# Фиксированная разрядность для всех целочисленных кодов.
# Это число используется сразу в нескольких модулях,
# поэтому его вынесли в отдельный файл, а не дублируют в коде.
BIT_WIDTH = 32

# Для деления в прямом коде будем хранить 5 двоичных знаков после точки.
DIVISION_FRAC_BITS = 5

# Параметры формата IEEE-754 binary32.
# 32 бита = 1 бит знака + 8 бит порядка + 23 бита мантиссы.
IEEE_TOTAL_BITS = 32
IEEE_EXP_BITS = 8
IEEE_MANTISSA_BITS = 23
